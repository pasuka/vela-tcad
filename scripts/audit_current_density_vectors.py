"""Audit Sentaurus/Vela current-density vector comparisons.

The input is the aligned node-field comparison CSV generated by
run_pn2d_coarse7x3_previous_full20_compare.py.  The audit intentionally works
from that neutral CSV so it can diagnose post-processing/export problems
without changing the coupled solve path.
"""

