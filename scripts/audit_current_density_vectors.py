def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--node-compare-csv", type=Path, default=DEFAULT_NODE_COMPARE)
    parser.add_argument("--elements-csv", type=Path, default=None)
    parser.add_argument("--mesh-json", type=Path, default=None)
    parser.add_argument("--terminal-csv", type=Path, default=None)
    parser.add_argument("--out-csv", type=Path, default=DEFAULT_OUT_CSV)
    parser.add_argument("--summary-md", type=Path, default=DEFAULT_SUMMARY)
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_arg_parser().parse_args(argv)
    nodes = read_compare(args.node_compare_csv)
    element_count = read_elements_count(args.elements_csv)
    vector_rows = build_vector_node_rows(nodes)
    vector_summary = build_vector_summary_rows(vector_rows)
    gava_rows = build_gava_rows(nodes)
    gava_summary = build_gava_summary_rows(gava_rows)
    meta_rows = [{
        "row_type": "input_summary",
        "node_count": len(nodes),
        "elements_count": element_count,
    }]
    contact_rows = contact_closure_rows(nodes, args.mesh_json, args.terminal_csv)
    all_rows = meta_rows + vector_rows + vector_summary + gava_rows + gava_summary + contact_rows
    write_csv(args.out_csv, all_rows)
    write_summary(args.summary_md, rows=all_rows, element_count=element_count)
    print(json.dumps({
        "node_count": len(nodes),
        "rows": len(all_rows),
        "out_csv": str(args.out_csv),
        "summary_md": str(args.summary_md),
    }, indent=2))
    return 0


