import csv
import json
import math
from pathlib import Path
from typing import Any


REPO = Path(__file__).resolve().parents[1]
DEFAULT_NODE_COMPARE = (
    REPO
    / "build-release/reference_tcad/pn2d_sentaurus2018_coarse7x3/reports"
    / "coarse_previous_full20_fields_20260629_155228/coarse_node_field_compare_aligned.csv"
)
DEFAULT_OUT_CSV = REPO / "build/diagnostics/current_density_vector_audit.csv"
DEFAULT_SUMMARY = REPO / "build/diagnostics/current_density_vector_audit_summary.md"
Q_C = 1.602176634e-19

VECTOR_PREFIXES = {
    "electric": "electric_field",
    "electron": "electron_current_density",
    "hole": "hole_current_density",
    "total": "total_current_density",
}

CURRENT_CARRIERS = ("electron", "hole", "total")

WINDOWS = {
    "full": (-math.inf, math.inf),
    "junction": (0.7, 1.3),
    "center": (0.9, 1.1),
    "left_shoulder": (0.7, 0.85),
    "right_shoulder": (1.15, 1.3),
}


def as_float(raw: str | None) -> float | None:
    if raw is None:
        return None
    text = str(raw).strip()
    if text == "":
        return None
    try:
        value = float(text)
    except ValueError:
        return None
    return value if math.isfinite(value) else None


def read_csv_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8-sig") as handle:
        return [
            {str(key).strip(): str(value).strip() for key, value in row.items()}
            for row in csv.DictReader(handle, skipinitialspace=True)
        ]


def read_compare(path: Path) -> dict[tuple[float, int], dict[str, Any]]:
    nodes: dict[tuple[float, int], dict[str, Any]] = {}
    for row in read_csv_rows(path):
        bias = as_float(row.get("bias_V"))
        node_id_raw = row.get("node_id", "")
        quantity = row.get("quantity", "")
        if bias is None or node_id_raw == "" or quantity == "":
            continue
        node_id = int(float(node_id_raw))
        key = (bias, node_id)
        entry = nodes.setdefault(key, {
            "bias_V": bias,
            "node_id": node_id,
            "x_um": as_float(row.get("x_um")),
            "y_um": as_float(row.get("y_um")),
            "values": {},
        })
        entry["values"][quantity] = {
            "sent": as_float(row.get("sentaurus_value")),
            "self": as_float(row.get("vela_value_scaled_to_sentaurus_units")),
        }
    return nodes


def get_value(node: dict[str, Any], quantity: str, side: str) -> float | None:
    data = node["values"].get(quantity)
    if data is None:
        return None
    return data.get(side)


def vector_for(node: dict[str, Any], prefix: str, side: str) -> tuple[float, float, float] | None:
    x = get_value(node, f"{prefix}_x", side)
    y = get_value(node, f"{prefix}_y", side)
    mag = get_value(node, f"{prefix}_mag", side)
    if x is None or y is None or mag is None:
        return None
    return x, y, mag


def safe_ratio(numerator: float | None, denominator: float | None) -> float | None:
    if numerator is None or denominator is None or denominator == 0.0:
        return None
    return numerator / denominator


def median(values: list[float]) -> float | None:
    finite = sorted(v for v in values if math.isfinite(v))
    if not finite:
        return None
    mid = len(finite) // 2
    if len(finite) % 2:
        return finite[mid]
    return 0.5 * (finite[mid - 1] + finite[mid])


def mean(values: list[float]) -> float | None:
    finite = [v for v in values if math.isfinite(v)]
    if not finite:
        return None
    return sum(finite) / len(finite)


def rms(values: list[float]) -> float | None:
    finite = [v for v in values if math.isfinite(v)]
    if not finite:
        return None
    return math.sqrt(sum(v * v for v in finite) / len(finite))


def rel_error(candidate: float | None, reference: float | None) -> float | None:
    if candidate is None or reference is None:
        return None
    scale = max(abs(reference), 1.0e-300)
    return abs(candidate - reference) / scale


def window_name(x_um: float | None) -> str:
    if x_um is None:
        return "unknown"
    for name in ("left_shoulder", "center", "right_shoulder"):
        lo, hi = WINDOWS[name]
        if lo <= x_um <= hi:
            return name
    if WINDOWS["junction"][0] <= x_um <= WINDOWS["junction"][1]:
        return "junction_other"
    return "outside_junction"


def build_vector_node_rows(nodes: dict[tuple[float, int], dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for (_bias, _node_id), node in sorted(nodes.items()):
        for carrier, prefix in VECTOR_PREFIXES.items():
            sent = vector_for(node, prefix, "sent")
            self_value = vector_for(node, prefix, "self")
            if sent is None or self_value is None:
                continue
            sx, sy, smag = sent
            vx, vy, vmag = self_value
            sent_hypot = math.hypot(sx, sy)
            self_hypot = math.hypot(vx, vy)
            dot = sx * vx + sy * vy
            cosine = dot / (sent_hypot * self_hypot) if sent_hypot > 0.0 and self_hypot > 0.0 else None
            rows.append({
                "row_type": "vector_node",
                "bias_V": node["bias_V"],
                "node_id": node["node_id"],
                "x_um": node["x_um"],
                "y_um": node["y_um"],
                "window": window_name(node["x_um"]),
                "carrier": carrier,
                "sent_x": sx,
                "sent_y": sy,
                "sent_mag": smag,
                "self_x": vx,
                "self_y": vy,
                "self_mag": vmag,
                "sent_hypot_xy": sent_hypot,
                "self_hypot_xy": self_hypot,
                "sent_mag_consistency_abs": abs(sent_hypot - smag),
                "self_mag_consistency_abs": abs(self_hypot - vmag),
                "cosine_similarity": cosine,
                "ratio_x": safe_ratio(vx, sx),
                "ratio_y": safe_ratio(vy, sy),
                "ratio_mag": safe_ratio(vmag, smag),
                "component_error_norm": math.hypot(vx - sx, vy - sy),
                "component_error_norm_signflip": math.hypot(-vx - sx, -vy - sy),
                "high_signal_value": max(abs(smag), abs(vmag)),
            })
    return rows


def in_window(row: dict[str, Any], name: str) -> bool:
    x = row.get("x_um")
    if x is None:
        return False
    lo, hi = WINDOWS[name]
    return lo <= float(x) <= hi


def fit_scale(rows: list[dict[str, Any]]) -> float | None:
    numerator = 0.0
    denominator = 0.0
    for row in rows:
        vx = row.get("self_x")
        vy = row.get("self_y")
        sx = row.get("sent_x")
        sy = row.get("sent_y")
        if None in (vx, vy, sx, sy):
            continue
        numerator += float(vx) * float(sx) + float(vy) * float(sy)
        denominator += float(vx) * float(vx) + float(vy) * float(vy)
    return numerator / denominator if denominator > 0.0 else None


def build_vector_summary_rows(vector_rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    groups: dict[tuple[float, str, str], list[dict[str, Any]]] = {}
    for row in vector_rows:
        for name in WINDOWS:
            if in_window(row, name):
                groups.setdefault((float(row["bias_V"]), str(row["carrier"]), name), []).append(row)
    for (bias, carrier, win), items in sorted(groups.items()):
        max_signal = max((float(item["high_signal_value"]) for item in items), default=0.0)
        threshold = max(max_signal * 1.0e-6, 1.0e-300)
        for sample_name, sample in [
            ("all", items),
            ("high_signal", [item for item in items if float(item["high_signal_value"]) >= threshold]),
        ]:
            if not sample:
                continue
            sign_err = [float(item["component_error_norm"]) for item in sample]
            flip_err = [float(item["component_error_norm_signflip"]) for item in sample]
            scale = fit_scale(sample)
            rows.append({
                "row_type": "vector_summary",
                "bias_V": bias,
                "carrier": carrier,
                "window": win,
                "sample": sample_name,
                "node_count": len(sample),
                "median_ratio_x": median([float(item["ratio_x"]) for item in sample if item.get("ratio_x") is not None]),
                "median_ratio_y": median([float(item["ratio_y"]) for item in sample if item.get("ratio_y") is not None]),
                "median_ratio_mag": median([float(item["ratio_mag"]) for item in sample if item.get("ratio_mag") is not None]),
                "max_mag_rel_error": max(
                    [rel_error(float(item["self_mag"]), float(item["sent_mag"])) or 0.0 for item in sample],
                    default=None,
                ),
                "median_cosine_similarity": median([
                    float(item["cosine_similarity"]) for item in sample if item.get("cosine_similarity") is not None
                ]),
                "rms_error_self_minus_sentaurus": rms(sign_err),
                "rms_error_neg_self_minus_sentaurus": rms(flip_err),
                "sign_flip_better": int((rms(flip_err) or math.inf) < (rms(sign_err) or math.inf)),
                "fit_scale_self_to_sentaurus": scale,
                "fit_scale_abs": None if scale is None else abs(scale),
                "median_sent_mag_consistency_abs": median([
                    float(item["sent_mag_consistency_abs"]) for item in sample
                ]),
                "median_self_mag_consistency_abs": median([
                    float(item["self_mag_consistency_abs"]) for item in sample
                ]),
            })
    return rows


def gava_rec(alpha_n: float | None, jn_mag: float | None, alpha_p: float | None, jp_mag: float | None, *, current_cm2: bool) -> float | None:
    if None in (alpha_n, jn_mag, alpha_p, jp_mag):
        return None
    value = (float(alpha_n) * abs(float(jn_mag)) + float(alpha_p) * abs(float(jp_mag))) / Q_C
    # Alpha is audited in m^-1.  If current is A/m^2, convert m^-3 s^-1 to cm^-3 s^-1.
    # If current is A/cm^2, first convert current density to A/m^2.
    return value * (1.0e-2 if current_cm2 else 1.0e-6)


def build_gava_rows(nodes: dict[tuple[float, int], dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for (_bias, _node_id), node in sorted(nodes.items()):
        for side in ("sent", "self"):
            alpha_n = get_value(node, "electron_alpha_avalanche", side)
            alpha_p = get_value(node, "hole_alpha_avalanche", side)
            jn = get_value(node, "electron_current_density_mag", side)
            jp = get_value(node, "hole_current_density_mag", side)
            exported = get_value(node, "avalanche", side)
            rec_m2 = gava_rec(alpha_n, jn, alpha_p, jp, current_cm2=False)
            rec_cm2 = gava_rec(alpha_n, jn, alpha_p, jp, current_cm2=True)
            if exported is None and rec_m2 is None and rec_cm2 is None:
                continue
            rows.append({
                "row_type": "gava_node",
                "bias_V": node["bias_V"],
                "node_id": node["node_id"],
                "x_um": node["x_um"],
                "y_um": node["y_um"],
                "window": window_name(node["x_um"]),
                "side": side,
                "alpha_n": alpha_n,
                "alpha_p": alpha_p,
                "jn_mag": jn,
                "jp_mag": jp,
                "gava_exported": exported,
                "gava_rec_A_per_m2_assumption": rec_m2,
                "gava_rec_A_per_cm2_assumption": rec_cm2,
                "gava_rec_rel_error_A_per_m2_assumption": rel_error(rec_m2, exported),
                "gava_rec_rel_error_A_per_cm2_assumption": rel_error(rec_cm2, exported),
            })
    return rows


